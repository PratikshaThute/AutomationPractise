package loop;

public class For_Loop {

	public static void main(String[] args) {
		 for (int a = 2; a<10;a++) {
			 System.out.println(a);
		 }
		 
		 System.out.println("------------------------------------------------------");
		 
//to get even numbers
		 for (int b = 0; b<30; b++) {
			 System.out.println(b*2);
		 }
		 
		 System.out.println("------------------------------------------------------");
		 
		//to get odd numbers
		 
		 for (int c = 0; c<15; c++) {
			 System.out.println(c*2+1);
		 }
		 System.out.println("--------------------------------------------------");
		 
//		 to get descending
		 for (int d = 20; d>10; d--) {
			 System.out.println(d);
		 }
	}

}
