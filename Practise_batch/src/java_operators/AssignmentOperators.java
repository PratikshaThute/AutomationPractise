package java_operators;

public class AssignmentOperators {

	public static void main(String[] args) {

		int a = 65;
		int b = a+20;
		System.out.println(b);
		
		double c = 85.50;
		double d = c - 20.25;
		System.out.println(d);
	
		float e = 5f;
		float f = e*10f;
		System.out.println(f);
		
		short g = 25;
		short h = (short) (g/5);
		System.out.println(h);
		
		int i = 96;
		int j = i/2;
		System.out.println(j);
	}

}
